    try {
        Connection conn = con.getCon();
        conn.setAutoCommit(false);
        
        String getMedicineIdSQL = "SELECT id_medicine FROM medicines WHERE name_medicine = ?";
        PreparedStatement pst1 = conn.prepareStatement(getMedicineIdSQL);
        pst1.setString(1, NameofMedicine);
        ResultSet rs1 = pst1.executeQuery();
        
        String medicineId = null;
        if (rs1.next()) {
            medicineId = rs1.getString("id_medicine");
        }
        rs1.close();
        pst1.close();

       
        String getCompanyIdSQL = "SELECT id_supplier FROM suppliers WHERE name_supplier = ?";
        PreparedStatement pst2 = conn.prepareStatement(getCompanyIdSQL);
        pst2.setString(1, NameofCompany);
        ResultSet rs2 = pst2.executeQuery();
        
        String companyId = null;
        if (rs2.next()) {
            companyId = rs2.getString("id_supplier");
        }
        rs2.close();
        pst2.close();

        int quantity = Integer.parseInt(Quantity);
        String transactionType = (String) Cmb_Transaction.getSelectedItem();

        String getStockQuantitySQL = "SELECT stock_quantity FROM medicines WHERE id_medicine = ?";
        PreparedStatement pst3 = conn.prepareStatement(getStockQuantitySQL);
        pst3.setString(1, medicineId);
        ResultSet rs3 = pst3.executeQuery();

        int currentStock = 0;
        if (rs3.next()) {
            currentStock = rs3.getInt("stock_quantity");
        }
        rs3.close();
        pst3.close();

        int newStock; 
        if (transactionType.equals("IN")){
            newStock = currentStock + quantity;
        } else {
            newStock = currentStock - quantity;
        }
        
        if (newStock < 0) {
            JOptionPane.showMessageDialog(null, "insufficient stock!");
            return;
        }


        String insertSQL = "INSERT INTO transactions (id_transaction, id_user, transaction_date, type_transaction, payment_total, payment_method) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement pst4 = conn.prepareStatement(insertSQL);
        pst4.setString(1, IDTransaction);
        pst4.setString(2, userIdSession);
        pst4.setString(3, TransactionDate);
        pst4.setString(4, TypeTransaction);
        pst4.setString(5, AmountPrice);
        pst4.setString(6, PaymentMethod);
        pst4.executeUpdate();
        pst4.close();
        
        int finalAmount = quantity * Integer.parseInt(PricePerUnit);
        
        String insertTransactionDetailsSQL = "INSERT INTO transaction_details (id_transaction_details, id_transaction, id_medicine, total_medicine, final_amount) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst5 = conn.prepareStatement(insertTransactionDetailsSQL);
        String IDTransactionDetail = generateTDetailsID();
        pst5.setString(1, IDTransactionDetail);
        pst5.setString(2, IDTransaction);
        pst5.setString(3, medicineId);
        pst5.setInt(4, quantity);
        pst5.setInt(5, finalAmount);
        pst5.executeUpdate();
        pst5.close();

        String updateStockSQL = "UPDATE medicines SET stock_quantity = ? WHERE id_medicine = ?";
        PreparedStatement pst6 = conn.prepareStatement(updateStockSQL);
        pst6.setInt(1, newStock);
        pst6.setString(2, medicineId);
        pst6.executeUpdate();
        pst6.close();
        
        conn.commit();
        conn.close();
        JOptionPane.showMessageDialog(null, "Transaction saved successfully!");
        setVisible(true);
                
        this.dispose();
        new SellMedicine().setVisible(true);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
