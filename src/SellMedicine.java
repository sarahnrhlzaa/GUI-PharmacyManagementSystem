    private HashMap<String, String> medicineMap = new HashMap<>();

    private void loadMedicineNames() {
        try {
            Connection conn = con.getCon();
            String sql = "SELECT id_medicine, name_medicine FROM medicines";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            String id = rs.getString("id_medicine");
            String name = rs.getString("name_medicine");
            medicineMap.put(name, id);
            Cmb_Medicine.addItem(name); 
        }
        rs.close();
        pst.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading medicines: " + e);
    }
  }
    
