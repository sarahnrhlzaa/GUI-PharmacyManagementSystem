    private String generateTransactionID() {
    String newID = "T001"; 
    try {
        Connection conn = con.getCon();
        String sql = "SELECT id_transaction FROM transactions ORDER BY id_transaction DESC LIMIT 1";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            String lastID = rs.getString("id_transaction"); 
            String olahID = lastID.substring(lastID.length() - 3);
            int process = Integer.parseInt(olahID) + 1;
            newID = "T" + String.format("%03d", process);
        }
        
        rs.close();
        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error generating transaction ID: " + e.getMessage());
    }
    return newID;
}
