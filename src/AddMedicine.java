    private String generateMedicineID() {
    String newID = "M001"; 
    try {
        Connection conn = con.getCon();
        String sql = "SELECT id_medicine FROM medicines ORDER BY id_medicine DESC LIMIT 1";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            String lastID = rs.getString("id_medicine"); 
            String olahID = lastID.substring(lastID.length() - 3);
            int process = Integer.parseInt(olahID) + 1;
            newID = "M" + String.format("%03d", process);
        }
        
        rs.close();
        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error generating medicine ID: " + e.getMessage());
    }
    return newID;
}


    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String NameofCompany = (String) Cmb_Company.getSelectedItem();
        String NameofMedicine = Txt_nameMedicine.getText();
        String Quantity = Txt_Quantity.getText();
        String PricePerUnit = Txt_priceUnit.getText();
        String ExpiredDate = Txt_expDate.getText();
        
        String IDSupplier = companyMap.get(NameofCompany);
        String IDMedicine = generateMedicineID();


        if(NameofCompany.equals("")){
            JOptionPane.showMessageDialog(null, "Name of company field is required!");
        } else if(NameofMedicine.equals("")){
            JOptionPane.showMessageDialog(null, "Name of medicine field is required!");
        } else if(Quantity.equals("")){
            JOptionPane.showMessageDialog(null, "Quantity field is required!");
        } else if(PricePerUnit.equals("")){
            JOptionPane.showMessageDialog(null, "Price per unit is required!");
        } else if(ExpiredDate.equals("")){
            JOptionPane.showMessageDialog(null, "Expired date of medicine is required!");
        } else{
            try {
                Connection conn = con.getCon();
                
                String checkMedicineSql = "SELECT id_medicine FROM medicines WHERE name_medicine = ?";
                PreparedStatement checkPst = conn.prepareStatement(checkMedicineSql);
                checkPst.setString(1, NameofMedicine);
                ResultSet rsCheck = checkPst.executeQuery();

                if (rsCheck.next()) {
                JOptionPane.showMessageDialog(null, "Medicine already exists!");
                return; 
            }

            rsCheck.close();
            checkPst.close();
            
                String sql = "INSERT INTO medicines (id_medicine, id_supplier, name_medicine, stock_quantity, price, expired_date) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, IDMedicine);
                pst.setString(2, IDSupplier);
                pst.setString(3, NameofMedicine);
                pst.setString(4, Quantity);
                pst.setString(5, PricePerUnit);
                pst.setString(6, ExpiredDate);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Medicine Added Succesfully");
                setVisible(true);
                pst.close();
                conn.close();
                
                this.dispose();
                new AddMedicine().setVisible(true);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } 
    }//GEN-LAST:event_saveBtnActionPerformed
 }
    
            
