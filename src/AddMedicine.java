    private HashMap<String, String> companyMap = new HashMap<>();

    private void loadCompany() {
        try {
            Connection conn = con.getCon();
            String sql = "SELECT id_supplier, name_supplier FROM suppliers";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            String id = rs.getString("id_supplier");
            String name = rs.getString("name_supplier");
            companyMap.put(name, id);
            Cmb_Company.addItem(name); 
        }
        rs.close();
        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading type transactions: " + e);
    }
}
